    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try {
            if (!Validator.onlyNumber(txtremuneracionI.getText())) {
                JOptionPane.showMessageDialog(this, "Remuneracion Invalidad, solo numeros de 4 digitos");
                return;
            }
            Empleado emp = new Empleado();
            emp.setNombres(txtnombresI.getText());
            emp.setApellidos(txtapellidosI.getText());
            emp.setRemuneracion(Double.parseDouble(txtremuneracionI.getText()));
            emp.setEdad(Integer.parseInt(txtedadI.getText()));
            if (emp.getEdad() < 18) {
                throw new InvalidDataEmpleadoException();
            }
            if (new EmpleadoController().insert(emp)) {
                JOptionPane.showMessageDialog(this, "Cliente Ingresado Correctamente!");
                listEmpleados();
            } else {
                JOptionPane.showMessageDialog(this, "Ha ocurrido un error");
            }
        } catch (SQLException | InvalidDataEmpleadoException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Alerta", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnbuscarUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarUActionPerformed
        try {
            Empleado emp =  new EmpleadoController().getEntity(Integer.parseInt(txtbuscarU.getText()));
            if (emp == null)
            {
                JOptionPane.showMessageDialog(this, "No se ha encontrado Resultados");
                return;
            }
            txtnombresU.setText(emp.getNombres());
            txtapellidosU.setText(emp.getApellidos());
            txtremuneracionU.setText(""+emp.getRemuneracion().intValue());
            txtedadU.setText(""+emp.getEdad());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        
    }//GEN-LAST:event_btnbuscarUActionPerformed

