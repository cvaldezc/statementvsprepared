    private void listEmpleados()
    {
        try {
         List<Empleado> emp = new EmpleadoController().getEntities();
         DefaultTableModel dtm = new DefaultTableModel(
                        new Object[][]{},
                        new String[]{"ID", "NOMBRES", "APELLIDOS", "REMUNERACION", "EDAD"
                        });
            for (Empleado e : emp) {
                dtm.addRow(new Object[]{e.getId(), e.getNombres(), e.getApellidos(), e.getRemuneracion(), e.getEdad()});
            }
            this.jtblresult.setModel(dtm);
        } catch (Exception e) {
        }
    }
    
    
